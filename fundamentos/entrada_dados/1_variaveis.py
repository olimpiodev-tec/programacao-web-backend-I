# Declarando variáveis em python

nome = input("Qual seu nome? ")
idade = int(input("Qual sua idade? "))
peso = int(input("Qual seu peso? "))
altura = float(input("Qual sua altura? "))

print(f"Sou {nome}, tenho {idade} anos")
print(f"Meu peso é {peso} e altura {altura}")

print(f"O tipo da variável nome é {type(nome)}")
print(f"O tipo da variável idade é {type(idade)}")
print(f"O tipo da variável peso é {type(peso)}")
print(f"O tipo da variável altura é {type(altura)}")
