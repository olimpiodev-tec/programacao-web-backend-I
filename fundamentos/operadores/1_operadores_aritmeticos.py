"""
Crie um algoritmo para as quatro operações aritméticas da calculadora
"""

n1 = 10
n2 = 2

soma = n1 + n2
subtracao = n1 - n2
multiplicacao = n1 * n2
divisao = n1 / n2
resto_divisao = n1 % 2

print(f"A soma dos numeros {n1} com {n2} é {soma}")
print(f"A subtração dos números {n1} com {n2} é {subtracao}")
print(f"A multiplicação dos números {n1} com {n2} é {multiplicacao}")
print(f"A divisão dos números {n1} com {n2} é {divisao}")
print(f"O resto da divisão dos números {n1} com {n2} é {resto_divisao}")
